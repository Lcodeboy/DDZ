package com.game.datacenter.memory;

public class MEMBroadcastNotify {
	
	private int notifyformat;
	
	private long notifytime;
	
	private String notifyinfo;
	
	private int id;

	public int getNotifyformat() {
		return notifyformat;
	}

	public void setNotifyformat(int notifyformat) {
		this.notifyformat = notifyformat;
	}

	public long getNotifytime() {
		return notifytime;
	}

	public void setNotifytime(long notifytime) {
		this.notifytime = notifytime;
	}

	public String getNotifyinfo() {
		return notifyinfo;
	}

	public void setNotifyinfo(String notifyinfo) {
		this.notifyinfo = notifyinfo;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
}
